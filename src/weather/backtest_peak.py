def _compute_actual_max_c(day_df: pd.DataFrame) -> Optional[float]:
    """End-of-day ground truth max °C: prefer 24h ASOS, fall back to max(temp_c)."""
    if "max_temp_24h_c" in day_df.columns:
        val = pd.to_numeric(day_df["max_temp_24h_c"], errors="coerce").max()
        if not np.isnan(val):
            return float(val)
    if "temperature_c" in day_df.columns:
        val = pd.to_numeric(day_df["temperature_c"], errors="coerce").max()
        if not np.isnan(val):
            return float(val)
    return None


def generate_day_samples(
    site: str,
    day_df: pd.DataFrame,
    precomp: dict,
    solar_noon_hour: float,
    actual_max_c: float,
    forecast_high_f: float,
    rng: np.random.RandomState,
) -> List[dict]:
    """3 random from [peak-2h, peak-~0], 3 near peak temp, 2 from [peak, peak+1h]."""
    hours = precomp["hours"]
    temps = precomp["temps"]
    running_max = precomp["running_max"]
    peak_hour = precomp["peak_hour"]
    samples = []

    def _sample(indices: np.ndarray, k: int) -> List[int]:
        if len(indices) <= k:
            return list(indices)
        return list(rng.choice(indices, size=k, replace=False))

    # 1) 3 random points from [peak-2h, peak) — approaching peak
    pre_mask = (hours >= peak_hour - 2.0) & (hours < peak_hour)
    pre_indices = np.where(pre_mask)[0]

    # 2) 3 points near peak temp (within 1°F of running max at that time)
    peak_temp = running_max[-1]  # final running max = daily high
    near_mask = (np.abs(temps - peak_temp) <= 1.0) & (hours >= peak_hour - 1.0) & (hours <= peak_hour + 1.0)
    near_indices = np.where(near_mask)[0]

    # 3) 2 random points from (peak, peak+1h] — just after peak
    post_mask = (hours > peak_hour) & (hours <= peak_hour + 1.0)
    post_indices = np.where(post_mask)[0]

    chosen = _sample(pre_indices, 3) + _sample(near_indices, 3) + _sample(post_indices, 2)

    for idx in chosen:
        feat = extract_peak_features(
            day_df, precomp, idx, solar_noon_hour, actual_max_c, forecast_high_f)
        if feat is not None:
            feat["site"] = site
            feat["date"] = str(day_df["date"].iloc[0])
            samples.append(feat)

    return samples


