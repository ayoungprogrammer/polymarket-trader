def load_all_samples(sites: Optional[List[str]] = None, since: Optional[str] = None) -> pd.DataFrame:
    """Load all sites, precompute momentum, generate samples."""

    if sites is None:
        sites = list(ALL_SITES)

    # Load solar noon cache
    solar_noon_lookup: Dict[Tuple[str, str], float] = {}
    if os.path.isfile(SOLAR_NOON_CSV):
        sn_df = pd.read_csv(SOLAR_NOON_CSV)
        for _, row in sn_df.iterrows():
            solar_noon_lookup[(row["site"], str(row["date"]))] = float(row["solar_noon_hour"])
        print(f"  Loaded {len(solar_noon_lookup)} solar noon entries")
    else:
        print(f"  No solar noon cache — using fallback (12.0)")

    # Load Open-Meteo historical forecast highs
    forecast_lookup = load_forecast_highs()
    n_forecast = len(forecast_lookup)
    if n_forecast:
        print(f"  Loaded {n_forecast} Open-Meteo forecast entries")
    else:
        print(f"  No Open-Meteo forecasts — using rolling 7-day fallback")

    # Load Open-Meteo hourly forecasts
    forecast_hourly_lookup = load_forecast_hourly()
    n_hourly = len(forecast_hourly_lookup)
    if n_hourly:
        print(f"  Loaded {n_hourly} Open-Meteo hourly forecast days")
    else:
        print(f"  No hourly forecasts — forecast curve features will use defaults")

    import time as _time
    rng = np.random.RandomState(42)
    all_rows: List[dict] = []
    total_days = 0
    skipped = 0
    n_forecast_hits = 0
    n_fallback = 0
    _t_load = 0.0
    _t_precomp = 0.0
    _t_samples = 0.0

    for site in sites:
        _t_site_start = _time.time()
        _t0 = _time.time()
        df = load_site_history(site)
        _t_load += _time.time() - _t0
        if df.empty:
            print(f"  {site}: no data")
            continue

        # Build rolling fallback for dates missing from Open-Meteo
        forecast_fallback = build_forecast_fallback(df)

        # Precompute per-day summaries for yesterday features
        all_day_groups = list(df.groupby("date"))
        day_groups = [(date, grp.sort_values("ts").reset_index(drop=True))
                      for date, grp in all_day_groups
                      if len(grp) >= 200 and (since is None or str(date) >= since)]
        skipped += len(all_day_groups) - len(day_groups)
        day_groups.sort(key=lambda x: x[0])

        # Build yesterday lookup: {date_str: summary_dict}
        day_summaries: Dict[str, dict] = {}
        for date, day_df in day_groups:
            temps_f = day_df["temperature_f"].astype(float)
            high_f = float(temps_f.max())
            low_f = float(temps_f.min())
            peak_idx = int(temps_f.idxmax())
            hours_arr = (pd.to_datetime(day_df["timestamp"].str[:19])
                         - pd.to_datetime(day_df["timestamp"].str[:10])).dt.total_seconds() / 3600.0
            peak_hour = float(hours_arr.iloc[peak_idx])
            date_str = str(date)
            om_fcst = forecast_lookup.get((site, date_str))
            fb_fcst = forecast_fallback.get(date_str, 70.0)
            fcst_high = om_fcst if om_fcst is not None else fb_fcst
            day_summaries[date_str] = {
                "high_f": high_f,
                "low_f": low_f,
                "peak_hour": peak_hour,
                "high_vs_forecast_f": high_f - fcst_high,
            }

        site_days = 0
        prev_summary: Optional[dict] = None
        for date, day_df in day_groups:
            actual_max_c = _compute_actual_max_c(day_df)
            if actual_max_c is None:
                skipped += 1
                prev_summary = day_summaries.get(str(date))
                continue

            date_str = str(date)
            om_val = forecast_lookup.get((site, date_str))
            if om_val is not None:
                forecast_high_f = om_val
                n_forecast_hits += 1
            else:
                forecast_high_f = forecast_fallback.get(date_str, 70.0)
                n_fallback += 1

            # Skip data up to any 6h METAR before 01:00 — its max/min
            # covers the previous evening and contaminates running accumulators.
            if "max_temp_6h_f" in day_df.columns:
                _early_metar = day_df[
                    day_df["max_temp_6h_f"].notna() & (day_df["ts"].dt.hour < 1)
                ]
                if not _early_metar.empty:
                    _cutoff_idx = _early_metar.index[-1] + 1
                    day_df = day_df.iloc[_cutoff_idx:].reset_index(drop=True)
                    if len(day_df) < 200:
                        skipped += 1
                        prev_summary = day_summaries.get(date_str)
                        continue

            _t0 = _time.time()
            precomp = precompute_day_momentum(day_df)
            # precompute_day_momentum already calls compute_momentum internally;
            # reuse its result instead of computing again (O(n²) per day).
            precomp["_mom_df"] = precomp.get("_mom_df")
            _t_precomp += _time.time() - _t0

            # Determine if this is a positive day (naive_f will shift +1)
            # to oversample it for class balance
            running_auto_max_c = precomp["running_auto_max_c"]
            final_auto_c = running_auto_max_c[~np.isnan(running_auto_max_c)]
            if len(final_auto_c) > 0:
                cur_naive_f = round(float(final_auto_c[-1]) * 9.0 / 5.0 + 32.0)
                actual_naive_f = round(float(actual_max_c) * 9.0 / 5.0 + 32.0)
                is_positive_day = actual_naive_f >= cur_naive_f + 1
            else:
                is_positive_day = False
            oversample = 2 if is_positive_day else 1

            _t0 = _time.time()
            sn = solar_noon_lookup.get((site, str(date)), 12.0)
            fh = forecast_hourly_lookup.get((site, date_str))
            samples = generate_day_samples(
                site, day_df, precomp, sn, actual_max_c, forecast_high_f, rng,
                oversample=oversample, forecast_hourly=fh, yesterday=prev_summary)
            all_rows.extend(samples)
            _t_samples += _time.time() - _t0
            site_days += 1
            prev_summary = day_summaries.get(date_str)

        total_days += site_days
        _t_site = _time.time() - _t_site_start
        print(f"  {site}: {site_days} days, {len(all_rows)} samples  ({_t_site:.1f}s)")
        sys.stdout.flush()

    print(f"  Total: {total_days} days, {len(all_rows)} samples "
          f"({skipped} days skipped)")
    print(f"  Forecast source: {n_forecast_hits} Open-Meteo, {n_fallback} rolling fallback")
    print(f"  Timing: load={_t_load:.1f}s  precomp={_t_precomp:.1f}s  samples={_t_samples:.1f}s")

    if not all_rows:
        return pd.DataFrame()
    return pd.DataFrame(all_rows)


