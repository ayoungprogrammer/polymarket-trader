def extract_peak_features(
    day_df: pd.DataFrame,
    precomp: dict,
    as_of_idx: int,
    solar_noon_hour: float,
    actual_max_c: float,
    forecast_high_f: float,
) -> Optional[dict]:
    """Extract features + label from one sample point within a day.

    Parameters
    ----------
    day_df : DataFrame
        Full day of observations (sorted by time).
    precomp : dict
        Output of precompute_day_momentum() for this day,
        augmented with ``_mom_df``.
    as_of_idx : int
        Index into the day's arrays — we only use data up to this point.
    solar_noon_hour : float
        Solar noon in decimal hours (e.g. 12.45).
    actual_max_c : float
        End-of-day 24h ASOS max °C (ground truth for label).
    forecast_high_f : float
        Forecast proxy: rolling 7-day mean of prior daily highs °F.
    """
    temps = precomp["temps"]
    hours = precomp["hours"]
    running_max = precomp["running_max"]
    ma_cross = precomp["ma_cross"]
    ma_short = precomp["ma_short"]
    ma_long = precomp["ma_long"]
    running_auto_max_c = precomp["running_auto_max_c"]
    n = precomp["n"]

    if as_of_idx < 2 or as_of_idx >= n:
        return None

    cur_temp = temps[as_of_idx]
    cur_hour = hours[as_of_idx]
    cur_max = running_max[as_of_idx]
    cur_max_c = running_auto_max_c[as_of_idx]

    if np.isnan(cur_temp) or np.isnan(cur_max) or np.isnan(cur_max_c):
        return None

    # --- Label ---
    will_increase = int(actual_max_c >= cur_max_c + 1)

    # --- Time features (5) ---
    ts_arr = day_df["ts"].values
    day_date = pd.Timestamp(ts_arr[0]).date()
    day_of_year = day_date.timetuple().tm_yday
    hour_decimal = cur_hour
    hours_since_solar_noon = cur_hour - solar_noon_hour
    day_of_year_sin = math.sin(2 * math.pi * day_of_year / 365.25)
    day_of_year_cos = math.cos(2 * math.pi * day_of_year / 365.25)
    pct_daylight_elapsed = max(0.0, min(1.0, (cur_hour - 6.0) / 12.0))

    # --- Temperature state (4) ---
    margin_below_max = cur_max - cur_temp
    max_indices = np.where(temps[:as_of_idx + 1] >= cur_max - 0.01)[0]
    last_max_idx = max_indices[-1] if len(max_indices) > 0 else as_of_idx
    time_since_max_hours = cur_hour - hours[last_max_idx]
    readings_since_max = as_of_idx - last_max_idx
    temp_range_so_far = cur_max - float(np.nanmin(temps[:as_of_idx + 1]))

    # --- Momentum (7) ---
    cur_ma_cross = float(ma_cross[as_of_idx]) if not np.isnan(ma_cross[as_of_idx]) else 0.0
    mom_df = precomp.get("_mom_df")
    if mom_df is not None and "rate_f_per_hr" in mom_df.columns:
        rate_val = mom_df["rate_f_per_hr"].iloc[as_of_idx]
        cur_rate = float(rate_val) if pd.notna(rate_val) else 0.0
    else:
        cur_rate = 0.0

    cur_ma_short = float(ma_short[as_of_idx]) if not np.isnan(ma_short[as_of_idx]) else cur_temp
    ma_short_minus_max = cur_ma_short - cur_max

    rate_30m_ago_idx = max(0, as_of_idx - 6)
    if mom_df is not None and "rate_f_per_hr" in mom_df.columns:
        r30 = mom_df["rate_f_per_hr"].iloc[rate_30m_ago_idx]
        rate_30m_ago = float(r30) if pd.notna(r30) else 0.0
    else:
        rate_30m_ago = 0.0
    rate_change = cur_rate - rate_30m_ago

    n_declining = 0
    for j in range(as_of_idx, last_max_idx, -1):
        if j > 0 and temps[j] < temps[j - 1]:
            n_declining += 1
        else:
            break
    max_decline_from_peak = cur_max - cur_temp

    # --- Peak shape (4) ---
    readings_at_max = int(np.sum(np.abs(temps[:as_of_idx + 1] - cur_max) < 0.01))
    at_max_indices = np.where(np.abs(temps[:as_of_idx + 1] - cur_max) < 0.01)[0]
    if len(at_max_indices) >= 2:
        time_at_max_hours = hours[at_max_indices[-1]] - hours[at_max_indices[0]]
    else:
        time_at_max_hours = 0.0
    readings_near_max_1f = int(np.sum(temps[:as_of_idx + 1] >= cur_max - 1.0))
    total_readings = as_of_idx + 1
    pct_at_or_near_max = readings_near_max_1f / total_readings if total_readings > 0 else 0.0

    # --- Trajectory (5) ---
    cur_ma_long = float(ma_long[as_of_idx]) if not np.isnan(ma_long[as_of_idx]) else cur_temp
    temp_above_ma_long = cur_temp - cur_ma_long

    def _slope_over(n_readings: int) -> float:
        start = max(0, as_of_idx - n_readings + 1)
        seg_temps = temps[start:as_of_idx + 1]
        seg_hours = hours[start:as_of_idx + 1]
        if len(seg_temps) < 2:
            return 0.0
        x = seg_hours - seg_hours[0]
        dx = x - x.mean()
        denom = np.sum(dx ** 2)
        if denom < 1e-10:
            return 0.0
        return float(np.sum(dx * (seg_temps - seg_temps.mean())) / denom)

    slope_2h = _slope_over(24)
    slope_1h = _slope_over(12)
    slope_30m = _slope_over(6)
    slope_change_2h_to_30m = slope_30m - slope_2h

    # --- Meteorological (5) ---
    wind_speed_mph = _safe_float(day_df, "wind_speed_mph", as_of_idx)
    dewpoint_f = _safe_float(day_df, "dewpoint_f", as_of_idx)
    relative_humidity_pct = _safe_float(day_df, "relative_humidity_pct", as_of_idx)
    pressure_tendency = _safe_float(day_df, "pressure_tendency", as_of_idx)
    wind_1h_ago_idx = max(0, as_of_idx - 12)
    wind_now = _safe_float(day_df, "wind_speed_mph", as_of_idx)
    wind_1h_ago = _safe_float(day_df, "wind_speed_mph", wind_1h_ago_idx)
    wind_change_1h = wind_now - wind_1h_ago

    # --- Forecast (3) ---
    forecast_gap_f = forecast_high_f - cur_max
    forecast_gap_pct = forecast_gap_f / forecast_high_f if forecast_high_f != 0 else 0.0
    current_vs_forecast = cur_temp - forecast_high_f

    return {
        # Time (5)
        "hour_decimal": round(hour_decimal, 3),
        "hours_since_solar_noon": round(hours_since_solar_noon, 3),
        "day_of_year_sin": round(day_of_year_sin, 4),
        "day_of_year_cos": round(day_of_year_cos, 4),
        "pct_daylight_elapsed": round(pct_daylight_elapsed, 4),
        # Temperature state (4)
        "margin_below_max": round(margin_below_max, 2),
        "time_since_max_hours": round(time_since_max_hours, 3),
        "readings_since_max": readings_since_max,
        "temp_range_so_far": round(temp_range_so_far, 2),
        # Momentum (7)
        "ma_cross": round(cur_ma_cross, 3),
        "rate_f_per_hr": round(cur_rate, 3),
        "ma_short_minus_max": round(ma_short_minus_max, 3),
        "rate_30m_ago": round(rate_30m_ago, 3),
        "rate_change": round(rate_change, 3),
        "n_declining_readings": n_declining,
        "max_decline_from_peak": round(max_decline_from_peak, 2),
        # Peak shape (4)
        "readings_at_max": readings_at_max,
        "time_at_max_hours": round(time_at_max_hours, 3),
        "readings_near_max_1f": readings_near_max_1f,
        "pct_at_or_near_max": round(pct_at_or_near_max, 4),
        # Trajectory (5)
        "temp_above_ma_long": round(temp_above_ma_long, 3),
        "slope_2h": round(slope_2h, 3),
        "slope_1h": round(slope_1h, 3),
        "slope_30m": round(slope_30m, 3),
        "slope_change_2h_to_30m": round(slope_change_2h_to_30m, 3),
        # Meteorological (5)
        "wind_speed_mph": round(wind_speed_mph, 2),
        "dewpoint_f": round(dewpoint_f, 2),
        "relative_humidity_pct": round(relative_humidity_pct, 2),
        "pressure_tendency": round(pressure_tendency, 3),
        "wind_change_1h": round(wind_change_1h, 2),
        # Forecast (3)
        "forecast_gap_f": round(forecast_gap_f, 2),
        "forecast_gap_pct": round(forecast_gap_pct, 4),
        "current_vs_forecast": round(current_vs_forecast, 2),
        # Label
        LABEL: will_increase,
    }


def _safe_float(df: pd.DataFrame, col: str, idx: int) -> float:
    """Get a float value from a DataFrame column at index, defaulting to 0."""
    if col not in df.columns:
        return 0.0
    val = df[col].iloc[idx]
    if pd.isna(val):
        return 0.0
    return float(val)


