def load_forecast_highs() -> Dict[Tuple[str, str], float]:
    """Load per-day Open-Meteo historical forecast highs from CSV.

    Returns {(site, date_str): forecast_high_f}.
    Generated by: cd src && python -m weather.analysis --fetch-forecasts
    """
    lookup: Dict[Tuple[str, str], float] = {}
    if os.path.isfile(FORECAST_HIGHS_CSV):
        df = pd.read_csv(FORECAST_HIGHS_CSV)
        for _, row in df.iterrows():
            lookup[(row["site"], str(row["date"]))] = float(row["forecast_high_f"])
    return lookup


def load_forecast_hourly() -> Dict[Tuple[str, str], List[Tuple[int, float]]]:
    """Load hourly Open-Meteo historical forecasts from CSV.

    Returns {(site, date_str): [(hour, temp_f), ...]}.
    Generated by: cd src && python -m weather.analysis --fetch-hourly-forecasts
    """
    lookup: Dict[Tuple[str, str], List[Tuple[int, float]]] = {}
    if os.path.isfile(FORECAST_HOURLY_CSV):
        df = pd.read_csv(FORECAST_HOURLY_CSV)
        for (site, date), grp in df.groupby(["site", "date"]):
            hours_temps = sorted(zip(grp["hour"].astype(int), grp["temperature_f"].astype(float)))
            lookup[(site, str(date))] = hours_temps
    return lookup


def build_forecast_fallback(site_df: pd.DataFrame, window: int = 7) -> Dict[str, float]:
    """Rolling 7-day mean of prior daily highs as fallback forecast proxy.

    Used only when Open-Meteo data is missing for a site-day.
    """
    daily = {}
    for date, grp in site_df.groupby("date"):
        temps = grp["temperature_f"].values.astype(float)
        if len(temps) > 0:
            daily[str(date)] = float(np.nanmax(temps))

    sorted_dates = sorted(daily.keys())
    proxy = {}
    for i, d in enumerate(sorted_dates):
        start = max(0, i - window)
        prior = [daily[sorted_dates[j]] for j in range(start, i)]
        if prior:
            proxy[d] = float(np.mean(prior))
        else:
            proxy[d] = daily[d]
    return proxy


