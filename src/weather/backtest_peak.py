def _compute_actual_max_c(day_df: pd.DataFrame) -> Optional[float]:
    """End-of-day ground truth max °C from 6h METAR readings (skip before 01:00)."""
    if "max_temp_6h_c" not in day_df.columns:
        raise ValueError("max_temp_6h_c column missing — re-fetch history data")
    _m6h = day_df[day_df["max_temp_6h_c"].notna() & (day_df["ts"].dt.hour >= 1)]
    if _m6h.empty:
        return None  # no daytime 6h METAR readings this day
    val = pd.to_numeric(_m6h["max_temp_6h_c"], errors="coerce").max()
    if np.isnan(val):
        return None
    return float(val)


def generate_day_samples(
    site: str,
    day_df: pd.DataFrame,
    precomp: dict,
    solar_noon_hour: float,
    actual_max_c: float,
    forecast_high_f: float,
    rng: np.random.RandomState,
    oversample: int = 1,
    forecast_hourly: Optional[List[Tuple[int, float]]] = None,
    yesterday: Optional[dict] = None,
) -> List[dict]:
    """3 random from [peak-2h, peak), 3 near peak temp, 2 from (peak, peak+1h].

    oversample: repeat sampling this many times (with replacement after first
    pass) to boost positive-day representation.
    """
    hours = precomp["hours"]
    temps = precomp["temps"]
    running_max = precomp["running_max"]
    peak_hour = precomp["peak_hour"]
    samples = []

    def _sample(indices: np.ndarray, k: int, allow_replace: bool = False) -> List[int]:
        if len(indices) == 0:
            return []
        if len(indices) <= k and not allow_replace:
            return list(indices)
        return list(rng.choice(indices, size=k, replace=allow_replace or len(indices) < k))

    # 1) [peak-2h, peak) — approaching peak
    pre_mask = (hours >= peak_hour - 2.0) & (hours < peak_hour)
    pre_indices = np.where(pre_mask)[0]

    # 2) Near peak temp (within 1°F of running max)
    peak_temp = running_max[-1]  # final running max = daily high
    near_mask = (np.abs(temps - peak_temp) <= 1.0) & (hours >= peak_hour - 1.0) & (hours <= peak_hour + 1.0)
    near_indices = np.where(near_mask)[0]

    # 3) (peak, peak+1h] — just after peak
    post_mask = (hours > peak_hour) & (hours <= peak_hour + 1.0)
    post_indices = np.where(post_mask)[0]

    chosen = _sample(pre_indices, 3) + _sample(near_indices, 3) + _sample(post_indices, 2)
    # Additional oversample passes with replacement
    for _ in range(oversample - 1):
        chosen += _sample(pre_indices, 3, allow_replace=True) \
                + _sample(near_indices, 3, allow_replace=True) \
                + _sample(post_indices, 2, allow_replace=True)

    for idx in chosen:
        feat = extract_peak_features(
            day_df, precomp, idx, solar_noon_hour, actual_max_c, forecast_high_f,
            forecast_hourly=forecast_hourly, yesterday=yesterday)
        if feat is not None:
            feat["site"] = site
            feat["date"] = str(day_df["date"].iloc[0])
            samples.append(feat)

    return samples


