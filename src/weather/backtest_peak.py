"""Peak detection backtest — predict if max °C will increase after time t.

Label: will_increase_c = 1 if the day's 24h ASOS max °C >= running_auto_max_c + 1
at sample time t. This predicts whether naive_f will shift, changing the bracket.

Sampling: 3 random from [peak-2h, peak) + 3 near peak temp + 2 from (peak, peak+1h].

Forecast proxy: rolling 7-day mean of prior daily highs (no lookahead).

Uses precompute_day_momentum() from backtest.py for efficient per-day computation.

Usage:
    cd src && python -m weather.backtest_peak
    cd src && python -m weather.backtest_peak --site KLAX,KMIA
    cd src && python -m weather.backtest_peak --loso
    cd src && python -m weather.backtest_peak --save
"""

