def _print_feature_importances(model, feature_cols: list, top_n: int = 15) -> None:
    importances = model.feature_importances_
    indices = np.argsort(importances)[::-1]

    print(f"\n  TOP {top_n} FEATURE IMPORTANCES")
    print(f"  {'-' * 40}")
    for rank, idx in enumerate(indices[:top_n], 1):
        print(f"  {rank:>2}. {feature_cols[idx]:<30s} {importances[idx]:.4f}")


def _print_per_site_accuracy(test_df: pd.DataFrame, y_pred: np.ndarray) -> None:
    meta = test_df.copy().reset_index(drop=True)
    meta["y_pred"] = y_pred

    print(f"\n  PER-SITE ACCURACY")
    print(f"  {'Site':<6} {'N':>6} {'Acc':>7} {'Pos%':>7}")
    print(f"  {'-'*6} {'-'*6} {'-'*7} {'-'*7}")

    for site in sorted(meta["site"].unique()):
        site_mask = meta["site"] == site
        site_df = meta[site_mask]
        y_t = site_df[LABEL].values
        y_p = site_df["y_pred"].values
        acc = (y_t == y_p).mean()
        pos = y_t.mean()
        print(f"  {site:<6} {len(site_df):>6} {acc:>7.4f} {pos:>6.1%}")


def _print_calibration(y_true: np.ndarray, y_prob: np.ndarray, n_bins: int = 10) -> None:
    print(f"\n  CALIBRATION TABLE ({n_bins} bins)")
    print(f"  {'Bin':>12s} {'Count':>7} {'Mean pred':>10} {'Mean actual':>12} {'Gap':>7}")
    print(f"  {'-'*12} {'-'*7} {'-'*10} {'-'*12} {'-'*7}")

    bin_edges = np.linspace(0, 1, n_bins + 1)
    for i in range(n_bins):
        lo, hi = bin_edges[i], bin_edges[i + 1]
        mask = (y_prob >= lo) & (y_prob < hi) if i < n_bins - 1 else (y_prob >= lo) & (y_prob <= hi)
        count = mask.sum()
        if count == 0:
            continue
        mean_pred = y_prob[mask].mean()
        mean_actual = y_true[mask].mean()
        gap = mean_pred - mean_actual
        print(f"  [{lo:.1f}, {hi:.1f}) {count:>7} {mean_pred:>10.4f} {mean_actual:>12.4f} {gap:>+7.4f}")


